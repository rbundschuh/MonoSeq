/* Homopolymer repeat calling */

/* declaration of sequence functions */

#ifndef __MSSEQUENCE_H
#define __MSSEQUENCE_H

/* sequences and patterns are stored as zero-terminated integer
   arrays with the following assignments: */

#define MONOSEQ_BASE_A 1
#define MONOSEQ_BASE_T 2
#define MONOSEQ_BASE_G 4
#define MONOSEQ_BASE_C 8

/* translate one character into an internal representation */
int IUPACToInternal(char c);

/* calculate complementary nucleotide */
int ComplementaryNucleotide(int n);

/* create sequence from IUPAC string */
int *CreateInternalSequence(const char *IUPACString);

/* create reverse complement */
int *CreateReverseComplement(const int *forward);

/* print sequence */
void PrintSequence(const int *sequence, FILE *fp);

#endif /* ndef __MSSEQUENCE_H */
